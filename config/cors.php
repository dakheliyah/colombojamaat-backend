    'paths' => ['api/*'],

    'allowed_methods' => ['*'],

    'allowed_origins' => (function () {
        $raw = env('CORS_ALLOWED_ORIGINS', 'http://localhost:5173');
        $arr = array_values(array_filter(array_map('trim', explode(',', (string) $raw))));

        return $arr !== [] ? $arr : ['http://localhost:5173'];
    })(),

    'allowed_headers' => ['*'],

    'supports_credentials' => true,
];